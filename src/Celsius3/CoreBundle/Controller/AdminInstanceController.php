namespace Celsius3\CoreBundle\Controller;

use Celsius3\CoreBundle\Helper\InstanceHelper;
use Sensio\Bundle\FrameworkExtraBundle\Configuration\Route;
use Sensio\Bundle\FrameworkExtraBundle\Configuration\Template;
use Sensio\Bundle\FrameworkExtraBundle\Configuration\Method;

/**
 * Instance controller.
 *
 * @Route("/admin/instance")
 */
class AdminInstanceController extends InstanceController
{

    /**
     * Displays a form to configure an existing Instance
     *
     * @Route("/configure", name="admin_instance_configure")
     * @Template()
     *
     * @param string $id The entity ID
     *
     * @return array
     *
     * @throws \Symfony\Component\HttpKernel\Exception\NotFoundHttpException If entity doesn't exists
     */
    public function configureAction()
    {
        return $this->baseConfigureAction($this->get('session')->get('instance_id'));
    }

    /**
     * Edits the existing Instance configuration.
     *
     * @Route("/{id}/update_configuration", name="admin_instance_update_configuration")
     * @Method("post")
     * @Template("Celsius3CoreBundle:AdminInstance:configure.html.twig")
     *
     * @param string $id The entity ID
     *
     * @return array
     *
     * @throws \Symfony\Component\HttpKernel\Exception\NotFoundHttpException If entity doesn't exists
     */
    public function configureUpdateAction($id)
    {
        return $this->baseConfigureUpdateAction($id, 'admin_instance');
    }

    /**
     * Edits the existing Instance configuration.
     *
     * @Route("/intercambio", name="admin_instance_intercambio")
     * @Method("get")
     * @Template("Celsius3CoreBundle:AdminInstance:intercambio.html.twig")
     *
     * @param string $id The entity ID
     *
     * @return array
     *
     * @throws \Symfony\Component\HttpKernel\Exception\NotFoundHttpException If entity doesn't exists
     */
    public function intercambioUIAction()
    {
        $instance = $this->get('celsius3_core.instance_helper')->getSessionOrUrlInstance();
        $paisRepository = $this->getDoctrine()->getManager()->getRepository('Celsius3CoreBundle:Country');
        $country = $paisRepository->findForInstanceAndGlobal($instance, $this->getDirectory())->getQuery()->execute();
        $institutionRepository = $this->getDoctrine()->getManager()->getRepository('Celsius3CoreBundle:Institution');
        $institutions = $institutionRepository->findForInstanceAndGlobal($instance, $this->getDirectory(), true)->getQuery()->execute();

        return array(
            'instituciones' => $institutions,
            'countries' => $country
        );
    }




}

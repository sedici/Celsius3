namespace Celsius3\TicketBundle\Entity;

use Symfony\Component\Validator\Constraints as Assert;
use Doctrine\ORM\Mapping as ORM;
use Gedmo\Timestampable\Traits\TimestampableEntity;
use Gedmo\Mapping\Annotation as Gedmo;

/**
 * @ORM\Entity("")
 * @ORM\Table(name="ticket_type_state")
 */
class TypeState
{

    const TYPE_STATE_NEW = 1;
    const TYPE_STATE_IN_PROGRESS = 2;



    use TimestampableEntity;
    /**
     * @ORM\Column(type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @Assert\NotBlank()
     * @ORM\Column(type="string", length=255)
     */
    private $typeState;


    public function __construct()
    {
    }

    /**
     * Get id
     *
     * @return id $id
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set typeState
     *
     * @param  string $typeState
     * @return self
     */
    public function setTypeState($typeState)
    {
        $this->typeState = $typeState;

        return $this;
    }

    /**
     * Get typeState
     *
     * @return string $typeState
     */
    public function getTypeState()
    {
        return $this->typeState;
    }


}
